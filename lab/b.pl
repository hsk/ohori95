  test(1):- <+(123+234)+> = <+R+>,R=123+234.
  test(1):- <+(<+(123+234)+>)+> = <+(<+R+>)+>,R=123+234.
  test(1):- writeln(<+123+> + <+123+>).
  test(1):- <+123+> + <+123+> = (<+R+>) + (<+R+>).
  %test(1):- <<123+234>> = <<R>>.
  test(1):- (<[123]>) = (<[R]>) , R=123.
  test(1):- (<[<[123]>,<[123]>]>) = <[<[R]>,<[R]>]>,R=123.
:- end_tests(<>).

